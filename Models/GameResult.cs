namespace chession.Models;

/// <summary>
/// Represents the outcome of a game.
/// </summary>
public enum GameResult
{
    /// <summary>
    /// The user won the game.
    /// </summary>
    Win,

    /// <summary>
    /// The user lost the game.
    /// </summary>
    Loss,

    /// <summary>
    /// The game ended in a draw.
    /// </summary>
    Draw
}
